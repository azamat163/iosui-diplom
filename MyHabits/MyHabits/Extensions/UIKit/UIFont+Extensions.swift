import Foundation
import UIKit

extension UIFont {
    class var semibold20: UIFont {
        return UIFont.systemFont(ofSize: 20, weight: .semibold)
    }
    
    class var semibold17: UIFont {
        return UIFont.systemFont(ofSize: 17, weight: .semibold)
    }
    
    class var regular17: UIFont {
        return UIFont.systemFont(ofSize: 17, weight: .regular)
    }
    
    class var semibold13: UIFont {
        return UIFont.systemFont(ofSize: 13, weight: .semibold)
    }
    
    class var regular13: UIFont {
        return UIFont.systemFont(ofSize: 13, weight: .regular)
    }
    
    class var regular12: UIFont {
        return UIFont.systemFont(ofSize: 12, weight: .regular)
    }
}
